    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        // TODO add your handling code here:
        int indice;
        indice = tblClientes.getSelectedRow();
        oCliente.setIdCliente((String) tblClientes.getValueAt(indice, 0));
        oCliente.setNombres((String) tblClientes.getValueAt(indice, 1));
        oCliente.setApellidos((String) tblClientes.getValueAt(indice, 2));
        oCliente.setDireccion((String) tblClientes.getValueAt(indice, 3));
        oCliente.setTelefono((String) tblClientes.getValueAt(indice, 4));
    }//GEN-LAST:event_tblClientesMouseClicked

