    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        // TODO add your handling code here:
        dato = txtDato.getText();
        if (rbtnCodigo.isSelected()) {
            modeloTabla = ClienteLogic.obtenerModeloTabla(dato);
           // tblClientes.setModel(modeloTabla);
        }else{
            if (rbtnNombre.isSelected()) {
                modeloTabla = ClienteLogic.obtenerModeloTablaporNombre(dato);
             //   tblClientes.setModel(modeloTabla);
            }else{
                modeloTabla = ClienteLogic.obtenerModeloTablaporApellido(dato);
               // tblClientes.setModel(modeloTabla);
            }
        }
        tblClientes.setModel(modeloTabla);

    }//GEN-LAST:event_btnVerActionPerformed

