    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:

        codigo = txtCodigo.getText();
        nombre = txtNombre.getText();
        costo = Double.parseDouble(txtCosto.getText());
        precio = Double.parseDouble(txtPrecio.getText());
        stock = Integer.parseInt(txtStock.getText());
        oCategoria = (Categoria) cboCategoria.getSelectedItem();

        Producto oProducto = new Producto();

        oProducto.setIdProducto(codigo);
        oProducto.setNombre(nombre);
        oProducto.setCosto(costo);
        oProducto.setPrecio(precio);
        oProducto.setStock(stock);
        oProducto.setoCategoria(oCategoria);

        if (ProductoLogic.insertarProducto(oProducto)) {
            System.out.println("se inserto correptamente");
            JOptionPane.showMessageDialog(this, "insercion correpta", "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
            num++;
            txtCodigo.setText("P00" + num);
            txtNombre.setText("");
            txtCosto.setText("");
            txtPrecio.setText("");
            txtStock.setText("");

        } else {
            System.out.println("no pudo se insertado");
            JOptionPane.showMessageDialog(this, "no pudo ser insertado", "MENSAJE", JOptionPane.INFORMATION_MESSAGE);

        }
        String nuevoProducto;
        nuevoProducto = codigo + " " + nombre + " "+costo+" "+ precio+" "+ stock+" "+ oCategoria+" \n";
        productosGuardar.add(nuevoProducto);
        modeloTabla = ProductoLogic.obtenerModeloTabla();
        tblProductos.setModel(modeloTabla);
    }//GEN-LAST:event_btnInsertarActionPerformed

